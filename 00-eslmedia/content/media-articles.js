  window.injectEslTeamsNav = async () => {
    ensureTeamMenuStyles();
    const directory = await loadTeamDirectory();

    const navLists = document.querySelectorAll(".topbar-nav, .site-topbar-nav");
    if (!navLists.length) return;

    const { contentPrefix } = getPathContext();
    navLists.forEach((navList) => {
      if (navList.querySelector(".esl-teams-nav")) return;

      const item = document.createElement("li");
      item.className = "esl-teams-nav";
      item.innerHTML = `
        <a href="${contentPrefix}teams.html">Teams</a>
        ${buildTeamMenuMarkup(directory)}
      `;

      navList.insertBefore(item, navList.children[1] || null);
    });

    document.querySelectorAll(".esl-teams-nav").forEach((item) => {
      if (item.dataset.teamsMenuBound === "true") return;
      item.dataset.teamsMenuBound = "true";

      let closeTimer = null;
      const openMenu = () => {
        window.clearTimeout(closeTimer);
        item.classList.add("is-open");
      };
      const scheduleClose = () => {
        window.clearTimeout(closeTimer);
        closeTimer = window.setTimeout(() => {
          item.classList.remove("is-open");
        }, 180);
      };

      item.addEventListener("mouseenter", openMenu);
      item.addEventListener("mouseleave", scheduleClose);
      item.addEventListener("focusin", openMenu);
      item.addEventListener("focusout", scheduleClose);
    });
  };

  const updateLatestLinks = () => {
    const latest = window.ESL_MEDIA_HELPERS.getLatestMonthInReview();
    if (!latest) return;

    const { contentPrefix } = getPathContext();
    const href = `${contentPrefix}${latest.file}`;
    document.querySelectorAll(".topbar-nav a, .site-topbar-nav a").forEach((link) => {
      if (link.textContent.trim().toLowerCase() !== "latest") return;
      link.setAttribute("href", href);
    });
  };

  const linkTopbarLogoHome = () => {
    const logoTarget = document.querySelector(".site-topbar-brand > div:last-child, .topbar-brand > div:last-child");
    if (!logoTarget || logoTarget.dataset.homeLinkBound === "true") return;

    const href = getMediaHomeHref();
    logoTarget.dataset.homeLinkBound = "true";
    logoTarget.setAttribute("role", "link");
    logoTarget.setAttribute("tabindex", "0");
    logoTarget.setAttribute("aria-label", "ESL Media Home");
    logoTarget.title = "ESL Media Home";
    logoTarget.closest(".site-topbar-brand, .topbar-brand")?.classList.add("is-home-link");

    const goHome = () => {
      window.location.href = href;
    };

    logoTarget.addEventListener("click", goHome);
    logoTarget.addEventListener("keydown", (event) => {
      if (event.key !== "Enter" && event.key !== " ") return;
      event.preventDefault();
      goHome();
    });
  };

  const init = async () => {
    linkTopbarLogoHome();
    updateLatestLinks();
    await window.injectEslTeamsNav();
  };

  if (!hasDocument) return;

  if (document.readyState === "loading") {
    document.addEventListener("DOMContentLoaded", init, { once: true });
  } else {
    init();
  }
